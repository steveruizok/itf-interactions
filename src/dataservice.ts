// Data Service

class DataService {
	constructor() {}

	apiUrl: string = 'https://itftennis.itfdataservices.com/'
	olympicApiUrl: string = 'https://itf.itfdataservices.com/api/olympics'
	olympicEvents = olympicEvents

	async fetchAndReturn(url: string) {
		let response = await fetch(url)
		return response.json()
	}

	getRandomPlayer = () => {
		return _.sample(olympicPlayers)
	}

	getRandomEvent = () => {
		return _.sample(olympicEvents)
	}

	getRandomMatch = () => {
		return _.sample(olympicMatches)
	}

	getOlympicEvent = (eventId: string) => {
		return this.fetchAndReturn(this.olympicApiUrl + `/events/${eventId}/`)
	}

	getOlympicPlayerProfile = (playerId: string) => {
		return this.fetchAndReturn(
			this.olympicApiUrl + `/players/${playerId}/profile`
		)
	}

	getOlympicPlayerActivity = (playerId: string) => {
		return this.fetchAndReturn(
			this.olympicApiUrl + `/players/${playerId}/activity`
		)
	}

	getCalendarEvents = (division: string = 'mt') => {
		return this.fetchAndReturn(this.apiUrl + `/calendar/${division}`)
	}

	getTournamentEvents = id => {
		return this.fetchAndReturn(this.apiUrl + `tournamentevents/${id}`)
	}

	getTournament = (id: string, division: string) => {
		return this.fetchAndReturn(this.apiUrl + `tournament/${id}/${division}`)
	}

	getDrawsResults = (id: string, round: string = '0') => {
		return this.fetchAndReturn(this.apiUrl + `drawsresults/${id}/${round}`)
	}
}

const dataservice = new DataService()

export default dataservice
